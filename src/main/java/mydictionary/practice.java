    private void searchTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextKeyReleased
        // TODO add your handling code here:
        if (searchText.getText().equals("")) {
            jLayeredPane1.setVisible(false);
        }
        else if (item1_sug.getText().equals("")) {
            jLayeredPane1.setVisible(false);
        }
        else { 
            jLayeredPane1.setVisible(true);
        }
        motion_editing.setVisible(false);
        if (y == 0) {
            motion.setVisible(false);
        }
        for (int i = 0; i < Dictionary.dat.size(); i++) {
            if (Dictionary.dat.get(i).word_target.toLowerCase().startsWith(searchText.getText())) {
                item1_sug.setText(Dictionary.dat.get(i).word_target);
                if (i+1 >= Dictionary.dat.size()) {
                   
                    break;
                }
                item2_sug.setText(Dictionary.dat.get(i+1).word_target);
                if (i+2 >= Dictionary.dat.size()) {
             
                    break;
                }
                item3_sug.setText(Dictionary.dat.get(i+2).word_target);
                if (i+3 >= Dictionary.dat.size()) {
           
                    break;
                }
                item4_sug.setText(Dictionary.dat.get(i+3).word_target);
                if(i+4 >= Dictionary.dat.size()){
                
                    break;
                } 
                item5_sug.setText(Dictionary.dat.get(i+4).word_target);
                break;
            }
            else {
                item1_sug.setText("");
                item2_sug.setText("");
                item3_sug.setText("");
                item4_sug.setText("");
                item5_sug.setText("");
            }
        }
    }//GEN-LAST:event_searchTextKeyReleased

